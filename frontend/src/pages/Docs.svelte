    <h2>Record Management</h2>
    The delivr.dev dashboard implements all the functionality of delivr through the API. Records can be added though the dashboard by clicking the "Add Record" button. Each record type has specific required attributes which will be displayed as input fields after selecting the record type from the dropdown.

    <h2>Node Pinning</h2>
    DNS records are propagated to every edge node by default. You can control which nodes have an individual record with Node Pinning. By clicking the location icon when adding a record, you can choose which nodes that the record will be served from.
